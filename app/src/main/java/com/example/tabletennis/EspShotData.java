package com.example.tabletennis;

class EspShotData {

    public int v1, v2, v3, delay;
    public float alpha, phi;

    EspShotData(int v1, int v2, int v3, float alpha, float phi, int delay) {
        this.v1 = v1;
        this.v2 = v2;
        this.v3 = v3;
        this.alpha = alpha;
        this.phi = phi;
        this.delay = delay;
    }


}
